    private void llenarTablaRanking(Map<String, Object> ranking) {
        modeloTablaRanking.setRowCount(0);

        ranking.forEach((nombre, datos) -> {
            // Realizar el cast expl√≠cito de Object a Object[]
            Object[] datosArray = (Object[]) datos;

            Object[] fila = new Object[]{
                nombre,
                datosArray[0], // Total ejercicios
                datosArray[1], // Ejercicios completados
                datosArray[2] // Porcentaje
            };
            modeloTablaRanking.addRow(fila);
        });

        modeloTablaRanking.fireTableDataChanged();
    }

